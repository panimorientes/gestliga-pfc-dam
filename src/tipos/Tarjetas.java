package tipos;

public class Tarjetas {
	int jug, equ, ama, roj;

	public Tarjetas() {
	}

	public Tarjetas(int jug, int equ, int ama, int roj) {
		this.jug = jug;
		this.equ = equ;
		this.ama = ama;
		this.roj = roj;
	}

	public int getJug() {
		return jug;
	}

	public void setJug(int jug) {
		this.jug = jug;
	}

	public int getEqu() {
		return equ;
	}

	public void setEqu(int equ) {
		this.equ = equ;
	}

	public int getAma() {
		return ama;
	}

	public void setAma(int ama) {
		this.ama = ama;
	}

	public int getRoj() {
		return roj;
	}

	public void setRoj(int roj) {
		this.roj = roj;
	}
	
	
}
