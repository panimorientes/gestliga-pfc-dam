package tipos;

public class MinutosJugador {
	int jug, par, min;

	public MinutosJugador() {
	}

	public MinutosJugador(int jug, int par, int min) {
		this.jug = jug;
		this.par = par;
		this.min = min;
	}

	public int getJug() {
		return jug;
	}

	public void setJug(int jug) {
		this.jug = jug;
	}

	public int getPar() {
		return par;
	}

	public void setPar(int par) {
		this.par = par;
	}

	public int getMin() {
		return min;
	}

	public void setMin(int min) {
		this.min = min;
	}
	
	
}
