package tipos;

public class Recuento {
	int jug, equ, goles;

	public Recuento() {
	}

	public Recuento(int jug, int equ, int goles) {
		this.jug = jug;
		this.equ = equ;
		this.goles = goles;
	}

	public int getJug() {
		return jug;
	}

	public void setJug(int jug) {
		this.jug = jug;
	}

	public int getEqu() {
		return equ;
	}

	public void setEqu(int equ) {
		this.equ = equ;
	}

	public int getGoles() {
		return goles;
	}

	public void setGoles(int goles) {
		this.goles = goles;
	}
	
	
}
