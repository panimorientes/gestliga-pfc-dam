package tipos;

public class GoleadorEquipos {
	int jug, equ, gol;

	public GoleadorEquipos() {
	}

	public GoleadorEquipos(int jug, int equ, int gol) {
		this.jug = jug;
		this.equ = equ;
		this.gol = gol;
	}

	public int getJug() {
		return jug;
	}

	public void setJug(int jug) {
		this.jug = jug;
	}

	public int getEqu() {
		return equ;
	}

	public void setEqu(int equ) {
		this.equ = equ;
	}

	public int getGol() {
		return gol;
	}

	public void setGol(int gol) {
		this.gol = gol;
	}
	
	
}
